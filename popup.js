  // Initialize on component mount
  useEffect(() => {
    init();
  }, []);

  // Initialize the popup
  async function init() {
    try {
      const { token, username } = await chrome.storage.sync.get(['token', 'username']);

      if (!token || !username) {
        setSetupNeeded(true);
        setLoading(false);
        return;
      }

      // Load PRs from storage
      const { previousPRs } = await chrome.storage.local.get(['previousPRs']);

      if (!previousPRs) {
        // If no data, trigger a refresh
        refreshData();
        return;
      }

      // Display PRs
      setPrs(previousPRs);
      setLoading(false);

    } catch (err) {
      setError(err.message);
      setLoading(false);
    }
  }

  // Refresh PR data
  function refreshData() {
    setLoading(true);

    // Send message to background script to check for updates
    chrome.runtime.sendMessage({ action: 'checkNow' }, async () => {
      try {
        const { previousPRs } = await chrome.storage.local.get(['previousPRs']);
        if (previousPRs) {
          setPrs(previousPRs);
        }
        setLoading(false);
      } catch (err) {
        setError(err.message);
        setLoading(false);
      }
    });
  }

